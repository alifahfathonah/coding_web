  <h1> Kota Malang </h1>
  </p> Malang, adalah sebuah kota/kabupaten di Jawa Timur. Kota ini terletak di dataran tinggi, 
  sehingga udara di kota ini cukup sejuk. Adapun budaya-budaya yang dapat kita jumpai di Malang 
  antara lain Wayang Topeng Malangan (Topeng Malang). Gaya kesenian ini adalah wujud pertemuan tiga 
  budaya (Jawa Tengahan, Madura, dan Tengger), Hal tersebut terjadi karena Malang memiliki tiga sub-kultur, 
  yaitu sub-kultur budaya Jawa Tengahan yang hidup di lereng gunung Kawi, sub-kultur Madura di lereng gunung 
  Arjuna, dan sub-kultur Tengger sisa budaya Majapahit di lereng gunung Bromo-Semeru. Etnik masyarakat Malang 
  terkenal religius, dinamis, suka bekerja keras, lugas dan bangga dengan identitasnya sebagai Arek Malang (AREMA) 
  serta menjunjung tinggi kebersamaan dan setia kepada Malang.Di kota Malang juga terdapat tempat yang merupakan sarana 
  apresiasi budaya Jawa Timur yaitu Taman Krida Budaya Jawa Timur, di tempat ini sering ditampilkan aneka budaya khas Jawa Timur 
  seperti Ludruk, Ketoprak, Wayang Orang, Wayang Kulit, Reog, Kuda Lumping, Sendra tari, saat ini bertambah kesenian baru yang kian 
  berkembang pesat di kota Malang yaitu kesenian “Bantengan” kesenian ini merupakan hasil dari kreatifitas masyarakat asli Malang, 
  sejak dahulu sebenarnya kesenian ini sudah dikenal oleh masyarakat Malang namun baru sekaranglah “Bantengan” lebih dikenal oleh 
  masyarakat tidak hanya masyarakat lokal namun juga luar daerah bahkan mancanegara. Khusus di Malang sering diadakan pergelaran 
  bantengan hampir setiap perayaan hari besar baik keagamaan maupun peringatan hari kemerdekaan.
Adapun jika kalian mencari makanan atau jajanan khas malang di sini kita bisa menemukan sangat banyak, 
yang paling terkenal antara lain Apel, Bakso Malang, Tempe dan kripik tempe khas Sanan, tahu lontong dan masih banyak yang lainnya.</p>
